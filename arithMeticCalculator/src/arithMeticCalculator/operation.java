package arithMeticCalculator;

public class operation {
	
	
	
	public int operationAddition(int a ,int b ) {
		return a + b;
	}
	
	public int operationSubstraction(int a ,int b ) {
		return a - b;
	}
	
	public int operationMultiplication(int a ,int b ) {
		return a * b;
	}
	
	public int operationDivision(int a ,int b ) {
		return a / b;
	}

}
